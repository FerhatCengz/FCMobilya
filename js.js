var sayac = 0;

function arti()
{
        sayac++;
        var sifir = document.getElementById("sifir").value = sayac;
        $("#linksepet").show(0500);
        $("#sifir").show(0500);
        $("#ekle").hide(0500);
        $("#cikar").show(0500);
        $("#id_urunkodu").show(0500);
 }



function eksi()
{                
        sayac--;
        var sifir = document.getElementById("sifir").value = sayac;
        $("#ekle").show(0500);
        $("#cikar").hide(0500);
        $("#id_urunkodu").hide(0500);
        if (sayac == 0)
        {

                  $("#linksepet").hide(0500);
                  $("#sifir").hide(0500);
               
        }
  
}

